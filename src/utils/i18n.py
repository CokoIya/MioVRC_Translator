def tr(language: str | None, key: str, **kwargs) -> str:
    normalized = language if language in UI_TEXTS else DEFAULT_UI_LANGUAGE
    template = UI_TEXTS.get(normalized, UI_TEXTS[DEFAULT_UI_LANGUAGE]).get(
        key,
        UI_TEXTS[DEFAULT_UI_LANGUAGE].get(key, key),
    )
    if kwargs:
        return template.format(**kwargs)
    return template
