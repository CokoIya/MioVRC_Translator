for _language, _texts in UI_TEXT_COMPLETIONS.items():
    UI_TEXTS.setdefault(_language, {}).update(_texts)

_SUPPORTED_UI_LANGUAGES = ("zh-CN", "en", "ja", "ru", "ko")


def _complete_ui_texts() -> None:
    all_keys = set().union(*(texts.keys() for texts in UI_TEXTS.values()))
    for language in _SUPPORTED_UI_LANGUAGES:
        table = UI_TEXTS.setdefault(language, {})
        for key in all_keys:
            table.setdefault(
                key,
                UI_TEXTS.get("en", {}).get(key, UI_TEXTS[DEFAULT_UI_LANGUAGE].get(key, key)),
            )


_complete_ui_texts()


def tr(language: str | None, key: str, **kwargs) -> str:
    normalized = language if language in UI_TEXTS else DEFAULT_UI_LANGUAGE
    template = UI_TEXTS.get(normalized, UI_TEXTS[DEFAULT_UI_LANGUAGE]).get(
        key,
        UI_TEXTS[DEFAULT_UI_LANGUAGE].get(key, key),
    )
    if kwargs:
        return template.format(**kwargs)
    return template
